    // ── HELPERS ───────────────────────────────────────────────────────────────

    private function createCategory(string $nameFr, string $slug, ?string $nameAr = null): Category
    {
        $category = (new Category())
            ->setNameFr($nameFr)
            ->setNameAr($nameAr)
            ->setSlug($slug)
            ->setActive(true);

        $this->entityManager->persist($category);
        $this->entityManager->flush();

        return $category;
    }

    private function createProductReferenceForCategory(Category $category): ProductReference
    {
        $brand = (new Brand())
            ->setCanonicalName('Brand for cat test')
            ->setSlug('brand-cat-test-'.$category->getId()->toRfc4122())
            ->setActive(true);
        $productReference = (new ProductReference())
            ->setBrand($brand)
            ->setCategory($category)
            ->setNameFr('Produit lié')
            ->setUnit(ProductUnit::Piece)
            ->setStatus(ProductReferenceStatus::Approved);

        $this->entityManager->persist($brand);
        $this->entityManager->persist($productReference);
        $this->entityManager->flush();

        return $productReference;
    }
}
