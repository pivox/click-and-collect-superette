<?php

declare(strict_types=1);

namespace App\Tests\Functional\Api;

use App\Entity\Brand;
use App\Entity\Category;
use App\Entity\ProductReference;
use App\Enum\ProductReferenceStatus;
use App\Enum\ProductUnit;

final class CategoryAdminApiTest extends FunctionalApiTestCase
{
    // ── LIST ──────────────────────────────────────────────────────────────────

    public function testAdminListsCategories(): void
    {
        $admin = $this->createUser('admin-cat-list@example.test', ['ROLE_ADMIN']);
        $cat = $this->createCategory('Produits laitiers', 'produits-laitiers');

        $response = $this->requestJson('GET', '/api/admin/categories', user: $admin);

        self::assertSame(200, $response->getStatusCode());
        $payload = $this->decodeJson($response);
        self::assertSame(1, $payload['total']);
        self::assertSame(1, $payload['page']);
        self::assertSame(20, $payload['limit']);
        self::assertCount(1, $payload['items']);
        self::assertSame($cat->getId()->toRfc4122(), $payload['items'][0]['id']);
        self::assertSame('Produits laitiers', $payload['items'][0]['name_fr']);
        self::assertSame('produits-laitiers', $payload['items'][0]['slug']);
        self::assertTrue($payload['items'][0]['is_active']);
        self::assertArrayHasKey('created_at', $payload['items'][0]);
        self::assertArrayHasKey('updated_at', $payload['items'][0]);
    }

    public function testListPaginationDefaultsAndLimit(): void
    {
        $admin = $this->createUser('admin-cat-pagination@example.test', ['ROLE_ADMIN']);
        for ($i = 1; $i <= 3; ++$i) {
            $this->createCategory('Catégorie '.$i, 'categorie-'.$i);
        }

        $pageOne = $this->decodeJson($this->requestJson('GET', '/api/admin/categories?page=1&limit=2', user: $admin));
        $pageTwo = $this->decodeJson($this->requestJson('GET', '/api/admin/categories?page=2&limit=2', user: $admin));

        self::assertSame(3, $pageOne['total']);
        self::assertSame(2, $pageOne['limit']);
        self::assertCount(2, $pageOne['items']);
        self::assertSame(3, $pageTwo['total']);
        self::assertCount(1, $pageTwo['items']);
    }

    // ── GET ITEM ──────────────────────────────────────────────────────────────

    public function testAdminGetsCategoryDetail(): void
    {
        $admin = $this->createUser('admin-cat-detail@example.test', ['ROLE_ADMIN']);
        $cat = $this->createCategory('Boissons', 'boissons', nameAr: 'مشروبات');

        $response = $this->requestJson('GET', \sprintf('/api/admin/categories/%s', $cat->getId()), user: $admin);

        self::assertSame(200, $response->getStatusCode());
        $payload = $this->decodeJson($response);
        self::assertSame($cat->getId()->toRfc4122(), $payload['id']);
        self::assertSame('Boissons', $payload['name_fr']);
        self::assertSame('مشروبات', $payload['name_ar']);
        self::assertSame('boissons', $payload['slug']);
        self::assertTrue($payload['is_active']);
        self::assertSame(0, $payload['sort_order']);
        self::assertNull($payload['parent_id']);
    }

    public function testGetCategoryReturns404WhenAbsent(): void
