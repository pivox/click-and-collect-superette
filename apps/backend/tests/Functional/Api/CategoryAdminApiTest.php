        self::assertSame(401, $this->requestJson('PATCH', \sprintf('/api/admin/categories/%s', $cat->getId()), ['nameFr' => 'X'])->getStatusCode());
        self::assertSame(401, $this->requestJson('DELETE', \sprintf('/api/admin/categories/%s', $cat->getId()))->getStatusCode());
    }

