    // ── DELETE ────────────────────────────────────────────────────────────────

    public function testAdminPhysicallyDeletesUnlinkedCategory(): void
    {
        $admin = $this->createUser('admin-cat-delete@example.test', ['ROLE_ADMIN']);
        $cat = $this->createCategory('À supprimer', 'a-supprimer');
        $id = $cat->getId()->toRfc4122();

        $deleteResponse = $this->requestJson('DELETE', \sprintf('/api/admin/categories/%s', $id), user: $admin);
        $getResponse = $this->requestJson('GET', \sprintf('/api/admin/categories/%s', $id), user: $admin);

        self::assertSame(204, $deleteResponse->getStatusCode());
        self::assertSame(404, $getResponse->getStatusCode());
    }

    public function testAdminLogicallyDeletesCategoryLinkedToProducts(): void
    {
        $admin = $this->createUser('admin-cat-logical-delete@example.test', ['ROLE_ADMIN']);
        $cat = $this->createCategory('Liée aux produits', 'liee-produits');
        $this->createProductReferenceForCategory($cat);
        $id = $cat->getId()->toRfc4122();

        $deleteResponse = $this->requestJson('DELETE', \sprintf('/api/admin/categories/%s', $id), user: $admin);
        $getResponse = $this->requestJson('GET', \sprintf('/api/admin/categories/%s', $id), user: $admin);

        self::assertSame(204, $deleteResponse->getStatusCode());
        self::assertSame(200, $getResponse->getStatusCode());
        self::assertFalse($this->decodeJson($getResponse)['is_active']);
    }

